    <h2>Snort Rulez使用说明</h2>
    <div>
	<p>在使用此应用程序之前，您应该：
	<ul>
	    <li>在Snort配置中添加规则文件 'easy.rules', 'custom.rules' 和 'test.rules'</li>
	    <li>已配置'incron'模块以自动更新传感器。</li>
	</ul>
	<p>如果您缺少任何这些步骤，请参阅文档： https://github.com/andoniaf/snort-Rulez </p>

	<h3><u>规则类型</u></h3>
	<ul>
	    <li><strong>测试规则</strong>: 将存储在'test.rules'中，旨在测试Snort的正常运行。建议在测试操作后将其消除。</li>
	    <li><strong>简易规则</strong>: 将存储在'test.rules'中，旨在测试Snort的正常运行。建议在测试操作后将其消除。</li>
	    <li><strong>自定义规则</strong>: 将存储在'test.rules'中，旨在测试Snort的正常运行。建议在测试操作后将其消除。</li>
	</ul>

	<h3><u>如何添加规则？</u></h3>
	<p>非常简单，访问您要添加的规则类型，在表单中选择所需的选项，然后点击 <em>"发送"</em>.<br>
	在表单下方，您可以看到已添加到相应 <em>"rules"</em> 文件中的规则.</p>

	<h3><u>如何删除规则？</u></h3>
	<p>在<em>文件内容..."</em>的右侧，有一个指向“查看表格”的链接，您可以从中删除不再需要的规则。<br>
	    也可以从<em>“查看规则表”</em>导航按钮查看这些表。</p>

